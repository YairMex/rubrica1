                            <p>
                                9.1 JUICIO DE CONCUBINATO, DEPENDENCIA ECONOMICA Y DESIGNACIÓN DE BENEFICIARIOS
                            </p>
                            <P>
                                Existen diversos casos en donde los trabajadores fallecidos, no se encontraban reconocidos 
formalmente ante la ley con su pareja, y las instituciones no dan paso adelante a otorgar los 
beneficios a la pareja, por el simple hecho de no haber algún documento que acredite la relación 
con el trabajador. Para esos casos, ofrecemos una solución especializada a través de un Juico de 
Concubinato Post Mortem, y con ello lograr tener acceso a los beneficios derivados de la viudez. 
Mientras que un juicio de designación de beneficiarios es aquel que se inicia cuando el trabajador 
falleció y son los beneficiarios tales como: espos@, concubin@, hijos, padres, etc, pueden hacer 
valer su derecho y poder reclamar las prestaciones que generó el trabajador en su empresa, administrado por las instituciones correspondientes,
a través de un juicio ante la Junta Federal de Conciliación y Arbitraje.
                            </P>
                            <BR>
                            <P>
                                9.2 INCONFORMIDADES
                            </P>
                            <P>
                                Cuando los trabajadores laboran formalmente, se incorporan al régimen obligatorio, con ello 
                                tienen derecho tanto ellos como su familia a recibir las prestaciones en especie y en dinero que 
                                otorga el IMSS, previo cumplimiento de los requisitos establecidos en la LSS. <BR>
                                En algunas ocasiones el Instituto llega a negar a sus derechohabientes los beneficios, por ejemplo: 
                                suspensión de pago, otorgamiento de una pensión por cesantía en edad avanzada o vejez, riesgo 
                                de trabajo (incapacidad permanente total o parcial), de invalidez, viudez u orfandad.<BR>
                                Dichas situaciones se resuelven por dos vías: la primera de ellas es a través del Recurso de 
                                Inconformidad en términos establecidos en el Reglamento del Recurso de Inconformidad (RRI), ante 
                                el Consejo Consultivo Delegacional competente del IMSS, en donde el Instituto es juez y parte, 
                                debido a que es un órgano administrativo el conocedor del recurso. Por lo que los resultados no 
                                siempre resultarán tan imparciales.<BR>
                                Mientras que el Juicio de Conflicto Individual de Seguridad Social, se promueve ante la Junta 
                                Federal de Conciliación y Arbitraje o ante el Tribunal Federal de Conciliación y Arbitraje, 
                                dependiendo del alcance de cada caso. 
